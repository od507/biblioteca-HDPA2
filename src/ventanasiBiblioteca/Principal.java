    private void solicitarLibro(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_solicitarLibro
        // TODO add your handling code here:
        
        try{
            String nombreUsuario = primerNombre.getText();
            String apellidoUsuario = apellido.getText();
            String titulo = nombreLibro.getText();
            
            int posLibro = Biblioteca.encontrarPosLibro(titulo);
            int posUsuario = Biblioteca.encontrarPosUsuario(nombreUsuario,apellidoUsuario);
        
            if (Biblioteca.verificaPrestarLibro(posLibro,posUsuario)){
            Biblioteca.prestarLibro(posLibro, posUsuario);
            mensaje.setText("El libro " + Biblioteca.listaLibros.get(posLibro).obtenerTitulo() + " sera prestado a " + Biblioteca.listaUsuarios.get(posUsuario).obtenerNombreCompleto());
        }
        else{
            if (!Biblioteca.verificarLibroDisp(posLibro)){
                mensaje.setText("Solamente nos queda " + Biblioteca.listaLibros.get(posLibro).obtenerCantidad() + " copia. No podemos prestarlo");
            }
            
            else{
                if (!Biblioteca.usuarioHabilitado(posUsuario)) {
                mensaje.setText("El usuario " + Biblioteca.listaUsuarios.get(posUsuario).obtenerNombreCompleto() + " ya tiene un libro en su posesion");
                }
            }
        }
        
        primerNombre.setText("");
        apellido.setText("");
        nombreLibro.setText("");
        }catch(Exception e){
            mensaje.setText("Debes cargar los datos de los libros y los usuarios primero.\n Para despues poder devolver o pedir libros");
        }
        
    }//GEN-LAST:event_solicitarLibro

