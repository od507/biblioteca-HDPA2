    private void devolverLibroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_devolverLibroActionPerformed
        // TODO add your handling code here:
        try{
            String nombreUsuario = primerNombre.getText();
            String apellidoUsuario = apellido.getText();
            String titulo = nombreLibro.getText();
        
            int posLibro = Biblioteca.encontrarPosLibro(titulo);
            int posUsuario = Biblioteca.encontrarPosUsuario(nombreUsuario,apellidoUsuario);
        
            Biblioteca.recibirLibro(posLibro, posUsuario);
            mensaje.setText("El libro " + Biblioteca.listaLibros.get(posLibro).obtenerTitulo() + " fue devuelto por " + Biblioteca.listaUsuarios.get(posUsuario).obtenerNombreCompleto());
            primerNombre.setText("");
            apellido.setText("");
            nombreLibro.setText("");
        }catch(Exception e){
            mensaje.setText("Debes cargar los datos de los libros y los usuarios primero. \nPara despues poder devolver o pedir libros");
        }
        
    }//GEN-LAST:event_devolverLibroActionPerformed

